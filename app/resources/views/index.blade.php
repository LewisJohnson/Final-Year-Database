<h2>Overview</h2>
<p>The final year projects should be viewed as the culmination of the degree - 
it gives students a chance to demonstrate all they have learned. 
It will be the most demanding part of the undergraduate degree. 
It is very different from most other courses. 
Although students will be supervised, they are on their own to a large extent. 
The onus is on the student to define the problem boundaries, to investigate possible solutions, and to present the results verbally, in writing and (possibly) to demonstrate in action. 
There will be no formal lectures to attend. 
The teaching methods used will simply be fortnightly individual/small group meetings to discuss progress. 
The results of the project will be submitted in report form to be examined and students will be expected to give a presentation/demonstration of their work, which will also be examined.
</p>

